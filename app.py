def add_numbers(num1, num2):
    """
    Function to add two numbers.
    
    Parameters:
    num1 (float or int): First number to be added.
    num2 (float or int): Second number to be added.
    
    Returns:
    float or int: Sum of the two input numbers.
    """
    return num1 + num2

# Example usage:
number1 = 5
number2 = 10
result = add_numbers(number1, number2)
print("The sum of", number1, "and", number2, "is:", result)
